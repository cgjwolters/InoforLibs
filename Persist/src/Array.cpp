void Array::write(DataWriter& dWrt) const
{
  dWrt.writeByte(Record_Type);

  int tp = ArrayType;
  short tps = (short)(tp & 0xFFFF);
  dWrt.writeShort(tps,"Array::write");
  dWrt.writeShort(elemType.id,"Array::write");
}

